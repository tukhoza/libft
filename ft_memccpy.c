#include "libft.h"

void	*ft_memccpy(void *dst, const void *src, int c, size_t n)
{
	size_t	i;
	char	*d;
	char	*s;

	i = 0;
	d = (char *)dst;
	s = (char *)src;
	while (i < n)
	{
		d[i] = s[i];
		if ((unsigned char)d[i] == (unsigned char)c)
			return ((void *)d + (i + 1));
		i++;
	}
	return (NULL);
}
