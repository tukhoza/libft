#include "libft.h"

void	ft_putnbr(int nb)
{
	if (nb <= -2147483648)
	{
		ft_putstr("-2147483648");
		return ;
	}
	if (nb < -9 || nb > 9)
		ft_putnbr(nb / 10);
	if (nb < 0)
	{
		if (nb >= -9 && nb <= 9)
			ft_putchar('-');
		ft_putchar('0' - (nb % 10));
	}
	else
		ft_putchar('0' + (nb % 10));
}
